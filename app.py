def reset_exam():
    """再挑戦時にステートをリセットする関数"""
    if 'exam_questions' in st.session_state:
        del st.session_state.exam_questions
    if 'exam_submitted' in st.session_state:
        del st.session_state.exam_submitted
    if 'score' in st.session_state:
        del st.session_state.score
    if 'user_answers' in st.session_state:
        del st.session_state.user_answers

def show_exam():
    st.title("🏆 まんてん！模擬試験（もぎしけん）")
    st.markdown("ここまで学んだことの総復習！**問題バンクの中から、毎回違う問題がランダムに10問出るよ！**\n**7問以上正解で「合格（ごうかく）」**だ！")

    # 初回アクセス時、またはリセット時にランダムな10問をセット
    if 'exam_questions' not in st.session_state:
        st.session_state.exam_questions = random.sample(QUESTIONS_BANK, 10)
        st.session_state.exam_submitted = False
        st.session_state.score = 0
        st.session_state.user_answers = []

    # テスト回答画面
    if not st.session_state.exam_submitted:
        with st.form("exam_form"):
            user_answers = []
            for i, q in enumerate(st.session_state.exam_questions):
                st.markdown(f"### 第{i+1}問")
                ans = st.radio(q["q"], q["opts"], index=None, key=f"q_{i}")
                user_answers.append(ans)

            submit_button = st.form_submit_button("答え合わせをする！")

        if submit_button:
            if None in user_answers:
                st.error("⚠️ まだ答えていない問題があるよ！ぜんぶ答えてからボタンを押してね。")
            else:
                score = 0
                for i, q in enumerate(st.session_state.exam_questions):
                    if user_answers[i] == q["a"]:
                        score += 1
                st.session_state.score = score
                st.session_state.user_answers = user_answers
                st.session_state.exam_submitted = True
                try:
                    st.rerun() # 画面を再描画して結果を表示
                except AttributeError:
                    st.experimental_rerun() # 古いバージョンのStreamlit用

    # 結果発表画面
    if st.session_state.exam_submitted:
        st.markdown("---")
        st.header(f"採点結果：10問中 【 {st.session_state.score} 問 】 正解！")
        
        if st.session_state.score >= 7:
            st.balloons()
            st.markdown("<div class='goukaku'>🎊 おめでとう！合格レベル達成！ 🎊</div>", unsafe_allow_html=True)
            st.success("素晴らしい！この調子で何度挑戦しても合格できるようになろう！")
        else:
            st.markdown("<div class='goukaku' style='color:blue;'>💪 あともう少し！ふくしゅうしよう 💪</div>", unsafe_allow_html=True)
            st.warning("まちがえた問題の解説をよく読んでから、もう一度挑戦してみよう！")

        st.markdown("### 📝 答えと解説")
        for i, q in enumerate(st.session_state.exam_questions):
            user_ans = st.session_state.user_answers[i]
            correct_ans = q["a"]
            
            with st.container():
                if user_ans == correct_ans:
                    st.success(f"**第{i+1}問：⭕ 正解！**\n\nあなたの答え：{user_ans}\n\n**解説：** {q['exp']}")
                else:
                    st.error(f"**第{i+1}問：❌ ざんねん！**\n\nあなたの答え：{user_ans} （正解は **{correct_ans}** ）\n\n**解説：** {q['exp']}")

        st.markdown("<br>", unsafe_allow_html=True)
        st.button("🔄 違う問題でもう一度挑戦する！", on_click=reset_exam, use_container_width=True)

if __name__ == "__main__":
    main()