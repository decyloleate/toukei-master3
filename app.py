    st.markdown("平均値が同じ「50点」のクラスでも、みんなが50点のクラスと、0点と100点しかいないクラスがある。その「散らばり具合」を調べるよ。")

    st.header("📏 どれくらい散らばっているか？")
    st.markdown("""
    - **範囲（レンジ）** ＝ 最大値（一番大きい数）から 最小値（一番小さい数）を引いたもの。
    - **分散（ぶんさん）** ＝ 散らばり具合を計算した特別な数。大きいほどバラバラ！
    - **標準偏差（ひょうじゅんへんさ）** ＝ 分散を使いやすくしたもの。3級では**「標準偏差が大きい＝データが広く散らばっている」**とだけ覚えればOK！
    """)
    
    st.markdown("---")
    st.header("📦 箱ひげ図（はこひげず）の読み方")
    st.markdown("データの散らばりを見るための便利なグラフだよ。これも3級で超重要！")
    
    np.random.seed(0)
