def show_correlation():
    st.title("4️⃣ ２つのデータの「かんけい」")
    st.markdown("「気温」と「アイスの売上」みたいに、2つのデータにどんな関係があるかを調べるよ。")
    
    st.header("📈 散布図（さんぷず）と相関（そうかん）")
    
    temp = np.random.uniform(15, 35, 30)
    ice_sales = temp * 5 + np.random.normal(0, 15, 30)
    
    df_corr = pd.DataFrame({"気温(℃)": temp, "アイスの売上(個)": ice_sales})
    
    # trendlineを描画するにはstatsmodelsが必要なため、今回は非表示にして点で傾向を示す
    fig_corr = px.scatter(df_corr, x="気温(℃)", y="アイスの売上(個)", title="気温とアイスの売上の散布図")
    st.plotly_chart(fig_corr, use_container_width=True)
    
    st.markdown("""
    点が**「右上がり」**になっているね。片方が増えると、もう片方も増える関係を**「正の相関（せいのそうかん）」**というよ。
    逆に、気温が上がると「おでんの売上」は減るよね。こういう関係は**「負の相関（ふのそうかん）」**というよ！
    
    これを「-1 から 1」の数字で表したのが**相関係数（そうかんけいすう）**。
    - **1 に近い** ＝ 強い「正」の相関（一直線に右上がり）
    - **-1 に近い** ＝ 強い「負」の相関（一直線に右下がり）
    - **0 に近い** ＝ 相関なし（バラバラ）
    """)

    st.markdown("---")
    st.header("📋 クロス集計表（しゅうけいひょう）")
    st.markdown("文字のデータ（男女、好き嫌いなど）の関係を調べる時は「クロス集計表」を使うよ。")
    
    df_cross = pd.DataFrame({
        "犬が好き": [15, 5, 20],
        "猫が好き": [10, 10, 20],
        "合計": [25, 15, 40]
    }, index=["男子", "女子", "合計"])
    
    st.table(df_cross)
    st.markdown("表を読んでみよう。「男子」で「犬が好き」な人は何人かな？ 答えは... **15人** だね！")

